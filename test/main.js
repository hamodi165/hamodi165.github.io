//Comment
function commentFunction() {
    var x = document.getElementById("comment-area");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }

  function replyFunction() {
    var x = document.getElementById("reply-area");
    if (x.style.display === "none") {
      x.style.display = "block";
    } else {
      x.style.display = "none";
    }
  }
    


